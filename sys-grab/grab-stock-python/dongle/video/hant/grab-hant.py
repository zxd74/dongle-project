    """
    获取视频片段
    @param video_list:
    @return:
    """
    for video in video_list:
        # 获取视频页信息
        vs = web + video['url']
        vc = request(vs)
        vh = BeautifulSoup(vc, "html.parser")
        src = vh.find('iframe')['src'].split('url=')[1]
        # 获取视频分段请求
        vc = request(src)
        video['url'] = host + vc.splitlines()[2]
    return video_list


