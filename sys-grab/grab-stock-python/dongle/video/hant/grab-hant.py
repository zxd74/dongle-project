def getVideoList():
    '''
    获取视频列表
    @return:
    '''
    content = request(url)
    html = BeautifulSoup(content, "html.parser")
    # title = html.find(class_="page-title").text.split("(")[0]
    # 获取视频列表
    video_list = []
    menu_list = html.find(class_='module-blocklist')
    for a in menu_list.find_all('a'):
        video_list.append({"url": a['href'], "title": a.find('sapn').text})
    return video_list


def downloadVideo(video_list):
    """
    下载视频，优化方向：多线程多用户
    @param video_list:
    @return:
    """
    for video in video_list:
        # 获取视频页信息
        vs = web + video['url']
        vc = request(vs)
        vh = BeautifulSoup(vc, "html.parser")
        src = vh.find('iframe')['src'].split('url=')[1]

        # 获取视频分段请求
        vc = request(src)
        src = host + vc.splitlines()[2]

        # 获取视频分片列表
        vc = request(src)
        split_list = []
        for s in vc.splitlines():
            if not s.startswith("#") and not len(s) == 0:
                split_list.append(s)

        # 下载分段视频 TODO 待优化：考虑多线程，多用户模式请求，即避免频次过高被限制，也加快执行效率
        print("开始下载视频")
        with open(path + "\\" + video['title'] + '.mp4', 'ab') as file:
            i = 0
            for s in split_list:
                # filename = s[-11:]
                v = requests.get(s).content
                file.write(v)
                file.flush()
                i += 1
                if i % 10 == 0:
                    print("已经下载%d/%d" % (i, len(split_list)))
                time.sleep(5)


