package com.iwanvi.nvwa.dao.model;

public class QuotaDid {
    private Integer id;

    private String fsId;

    private Integer creId;

    private String did;

    private Integer isActive;

    private Long clkTime;

    private Long actTime;

    private String ip;

    private String areaCode;

    private String aid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFsId() {
        return fsId;
    }

    public void setFsId(String fsId) {
        this.fsId = fsId;
    }

    public Integer getCreId() {
        return creId;
    }

    public void setCreId(Integer creId) {
        this.creId = creId;
    }

    public String getDid() {
        return did;
    }

    public void setDid(String did) {
        this.did = did;
    }

    public Integer getIsActive() {
        return isActive;
    }

    public void setIsActive(Integer isActive) {
        this.isActive = isActive;
    }

    public Long getClkTime() {
        return clkTime;
    }

    public void setClkTime(Long clkTime) {
        this.clkTime = clkTime;
    }

    public Long getActTime() {
        return actTime;
    }

    public void setActTime(Long actTime) {
        this.actTime = actTime;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getAid() {
        return aid;
    }

    public void setAid(String aid) {
        this.aid = aid;
    }
}