package com.iwanvi.nvwa.pixel.connector.common.utils;

public class IPRow {
	
	private Long begin;
	
	private Long end;

	private String code;

	public Long getBegin() {
		return begin;
	}

	public void setBegin(Long begin) {
		this.begin = begin;
	}

	public Long getEnd() {
		return end;
	}

	public void setEnd(Long end) {
		this.end = end;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
